// add x-y
function add(x,y) {
    return x + y;
}
a =add(2,4);
console.log(a);

// subtract x-y
function subtract(x,y) {
    return x - y;
}
b =subtract(5,2);
console.log(b);

// multiply x-y
function multiply(x,y) {
    return x * y;
}
c =multiply(2,4);
console.log(c);

// divide x/y
function divide(x,y) {
    return x / y;
}
d =divide(10,2);
console.log(d);